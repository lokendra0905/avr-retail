import fs from "fs";
import path from "path";

const ROOT = path.resolve("public/assets");
const OUT = path.join(ROOT, "categories");

/** Flat photo folders → clean URL slugs */
const CATEGORIES = [
  { folder: "garments", slug: "garments-showroom-design", title: "Garments Showroom Design" },
  { folder: "jewallery store", slug: "jewellery-showroom-design", title: "Jewellery Showroom Design" },
  { folder: "shoes showroom", slug: "shoe-showroom-design", title: "Shoe Showroom Design" },
  { folder: "mobile showroom", slug: "mobile-showroom-design", title: "Mobile Showroom Design" },
  { folder: "Super mart", slug: "supermarket-design", title: "Supermarket Design" },
  { folder: "Gift and toyshowroom", slug: "gift-showroom-design", title: "Gift & Toy Showroom Design" },
  { folder: "madical store design", slug: "medical-store-design", title: "Medical Store Design" },
  { folder: "watch shoroom", slug: "watch-showroom-design", title: "Watch Showroom Design" },
];

const IMAGE_EXT = new Set([".jpg", ".jpeg", ".png", ".webp", ".JPG", ".JPEG", ".PNG", ".WEBP"]);

function listImages(dir) {
  if (!fs.existsSync(dir)) return [];
  return fs
    .readdirSync(dir)
    .filter((f) => {
      const full = path.join(dir, f);
      return fs.statSync(full).isFile() && IMAGE_EXT.has(path.extname(f));
    })
    .sort();
}

const generated = [];

if (fs.existsSync(OUT)) {
  fs.rmSync(OUT, { recursive: true });
}
fs.mkdirSync(OUT, { recursive: true });

for (const cat of CATEGORIES) {
  const srcDir = path.join(ROOT, cat.folder);
  const destDir = path.join(OUT, cat.slug);
  const images = listImages(srcDir);

  if (images.length === 0) {
    console.warn(`No images for ${cat.folder}`);
    continue;
  }

  fs.mkdirSync(destDir, { recursive: true });
  const urls = [];

  images.forEach((file, i) => {
    const ext = path.extname(file).toLowerCase() || ".jpg";
    const destName = `${cat.slug}-${String(i + 1).padStart(2, "0")}${ext}`;
    fs.copyFileSync(path.join(srcDir, file), path.join(destDir, destName));
    urls.push(`/assets/categories/${cat.slug}/${destName}`);
  });

  generated.push({
    slug: cat.slug,
    title: cat.title,
    coverImage: urls[0],
    galleryImages: urls,
  });

  console.log(`✓ ${cat.title}: ${urls.length} images`);
}

const outFile = path.resolve("constants/category-galleries.generated.ts");
const content = `// Auto-generated by scripts/prepare-category-assets.mjs — do not edit manually
export type GeneratedCategoryGallery = {
  slug: string;
  title: string;
  coverImage: string;
  galleryImages: string[];
};

export const CATEGORY_GALLERIES: GeneratedCategoryGallery[] = ${JSON.stringify(generated, null, 2)};

export function getCategoryGallery(slug: string): GeneratedCategoryGallery | undefined {
  return CATEGORY_GALLERIES.find((c) => c.slug === slug);
}
`;

fs.writeFileSync(outFile, content);
console.log(`\nWrote ${outFile}`);
